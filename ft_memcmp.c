#include "libft.h"

int	ft_memcmp(const void *str1, const void *str2, size_t n)
{
	unsigned char	*s1;
	unsigned char	*s2;
	size_t			count;

	s1 = (unsigned char*)str1;
	s2 = (unsigned char*)str2;
	count = 0;
	while (count < n - 1)
	{
		if (count < n && s1[count] != s2[count])
			return (s1[count] - s2[count]);
		count++;
	}
	return (s1[count] - s2[count]);
}
