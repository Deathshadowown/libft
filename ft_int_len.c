#include "libft.h"

int	ft_int_len(int n)
{
	int i;

	i = 0;
	if (n < 0)
	{
		i++;
		n *= -1;
	}
	if (n == 0)
	{
		return (1);
	}
	while (n > 0)
	{
		i++;
		n /= 10;
	}
	return (i);
}
